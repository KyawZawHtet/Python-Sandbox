a = 10
b = 3
print("Sum is", a + b, type(a + b))
print("Subtract is", a - b, type(a - b))
print("Multiply is", a * b, type(a * b))

# Note: Division always results in a float
print("Divide is", a / b, type(a / b))

# Integer Division
print("Integer Division is", a // b, type(a // b))
print("Power is", a ** b, type(a ** b))
print("Modulus is", a % b, type(a % b))
